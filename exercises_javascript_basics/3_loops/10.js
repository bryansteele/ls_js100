let counter = 0;

while (counter > 0) {
  console.log('Woooot!');
  counter -= 1;
}
// while loop checks the conditional before it executes the code.

let counter = 0;

do {
  console.log('Woooot!');
  counter -= 1;
} while (counter > 0);
// do/while loop will execute code at least once before it checks the condition